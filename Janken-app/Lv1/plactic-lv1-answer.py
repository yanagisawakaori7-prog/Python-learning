# 🔹 このファイルでは「1回だけ勝負する」じゃんけんを作ります
# 🔹 ポイントは「入力」「乱数」「条件分岐」の3つだけ

# Pythonの標準ライブラリ「random」を使う
import random

# 出せる手をリストで定義
# グー、チョキ、パーを、handsリストに格納する
hands = ["グー", "チョキ", "パー"]

# ユーザーに入力してもらう
# 下記の２文を出力する
# 「じゃんけんをしましょう！
# 「0: グー, 1: チョキ, 2: パー」
print("じゃんけんをしましょう！")
print("0: グー, 1: チョキ, 2: パー")

# ここでユーザーの入力を受け取る
# input() は文字列を返すので、int() で数値に変換する
# player という値に格納する
player = int(input("数字を入力してください："))

# コンピュータの手をランダムに決める
# randint(0, 2) → 0〜2のいずれかを返す
# computer という値に格納する
computer = random.randint(0, 2)

# それぞれの手を表示
# 下記２文を出力する
# 「あなたの手：{playerの手}」
# 「コンピュータの手：{computerの手}」
print("あなたの手：", hands[player])
print("コンピュータの手：", hands[computer])


# 勝敗を判定する

# 「同じ手」なら引き分け
if player == computer:
    print("あいこです！")

# 「勝ちパターン」を3つの条件で書く
elif (player == 0 and computer == 1) or \
     (player == 1 and computer == 2) or \
     (player == 2 and computer == 0):
    print("あなたの勝ちです！")

# それ以外は負け
else:
    print("あなたの負けです！")
